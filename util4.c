_BOOL interrupted(void)
{
	if ((Kbshift(-1) & 3) == 3)
		return TRUE;
	return FALSE;
}

/* ---------------------------------------------------------------------- */

void get_date(int *day, int *month, int *year)
{
	short date;
	
	date = Tgetdate();
	*day = date & 0x1f;
	*month = (date >> 5) & 0x0f;
	*year = ((date >> 9) & 0x7f) + 1980;
}
